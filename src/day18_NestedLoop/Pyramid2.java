package day18_NestedLoop;

public class Pyramid2 {
    public static void main(String[] args) {

        int h = 9;


        for (int i = 0; i < h; i++) {

            for (int j = 0; j <= h-i-1; j++ )
                System.out.print(" ");
            for (int m = 0; m < i+1; m++)
                System.out.print("*");


            for (int o = 0; o < i+1; o++)
                System.out.print("*");








                System.out.println("");









        }







    }
}
